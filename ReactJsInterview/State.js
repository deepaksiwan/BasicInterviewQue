
// State:
// State is managed within the component itself and is used to store data that can change over time.
// State is initialized in the constructor of a class component or using the useState hook in a functional component.
// State can only be updated using the setState() method.
// Changing state triggers the re-rendering of the component and its children.

// Props:
// Props are passed from a parent component to a child component as read-only data.
// Props are defined as attributes in the parent component's JSX.
// Props can be accessed in the child component using the props object.
// Props cannot be modified within the child component.



























